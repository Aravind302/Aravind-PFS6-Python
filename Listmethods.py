#List Methods
x=[12,52.3,"akki",True]
#Append
x.append(5)
print(x)
x.append([4,5,6])
print(x)
#extend()
x.extend([4,5,6])
print(x)
#insert()
a=[7,8,9,5,5]
a.insert(3,10)
print(a)
#remove()
a.remove(5)
print(a)
#pop()
a.pop()
print(a)
#index()
b=[45,56,23,78,25,14]
print(b.index(78))
print(b)
#clear()
b.clear()
print(b)
#count
c=[7,8,8,5,2,74,8,6,14]
print(c.count(8))
#sort()
z=[56,41,78,569,1254,7,156,456,415]
z.sort()
print(z)
#reverse()
z.reverse()
print(z)
#copy()
n=[5,56,48]
s=n.copy()
print(s)
