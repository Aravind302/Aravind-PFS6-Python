#DATA TYPES
#Built in function=type()
#Text Types
#str
a="aravind"
b="45"
print(type(a))
print(type(b))
#Numeric types
#int
a=456
print(type(a))
print(a)
#float
a=52.23
print(type(a))
print(a)
#complex
a=2+3j
print(type(a))
print(a)
#Sequence types
#list
#collection of elements
a=[]
print(type(a))
print(a)
b=[1,2,3,5]
print(type(b))
print(b)
c=["a",12,"nut",56.2]
print(type(c))
print(c)
#tuple
a=()
print(type(a))
b=(23,"asd",456.23)
print(type(b))
#Mapping types
#dict
a={1:10,2:26}
print(type(a))
b={"name":"aravind","age":23}
print(type(a))
#Set types
#set
a={45,"a","asd",52.3}
print(type(a))
print(a)
b={1,2,23,2,23,"adf","adfr"}
print(type(b))
print(b)
#Frozen set
a = frozenset({1,2,3,4})
print(a)
print(type(a))
b = frozenset({"a","b","c","d"})
print(b)
print(type(b))
c = frozenset({1,1,2,3,"a","b","a","c"})
print(c)
print(type(c))
#Boolean Type
a = True
print(a)
print(type(a))
b = False
print(b)
print(type(b))











